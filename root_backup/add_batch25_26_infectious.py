def format_trial(trial, idx):
    """Format a single trial as JavaScript object"""

    # Determine design type
    design = trial.get('design', 'Superiority')

    # Build the text narrative
    if trial['effect_type'] in ['MD', 'mean difference']:
        effect_text = f"mean difference {trial['effect_value']}, 95% CI {trial['ci_lo']}-{trial['ci_hi']}"
    elif trial['effect_type'] == 'RD':
        effect_text = f"Risk difference {trial['effect_value']}%, 95% CI {trial['ci_lo']}-{trial['ci_hi']}"
    else:
        effect_text = f"{trial['effect_type']} {trial['effect_value']:.2f}, 95% CI {trial['ci_lo']:.2f}-{trial['ci_hi']:.2f}"

    # Add non-inferiority text if applicable
    ni_text = ""
    if trial.get('ni_met') is True:
        ni_text = " Non-inferiority met."
    elif trial.get('ni_met') is False:
        ni_text = " Non-inferiority not met."

    text = f"""{trial['id']}: {trial['treatment_arm']} vs {trial['control_arm']}.
Patients randomized to treatment ({trial['treatment_arm']}, n={trial['n_treatment']}) versus control ({trial['control_arm']}, n={trial['n_control']}).
The primary endpoint was {trial['primary_endpoint']}. Mean age was {trial['age_mean']} years, {trial['male_pct']}% were male.
Results: {effect_text}. P={trial['p_value']:.3f}.{ni_text}
Follow-up was {trial['followup']}. Trial registration: {trial['registration']}."""

    # Escape backticks and other special chars
    text = text.replace('`', "'").replace('\\', '\\\\')

    # Build ground truth
    ground_truth = {
        'primaryEffect': {
            'type': trial['effect_type'],
            'value': trial['effect_value'],
            'ciLo': trial['ci_lo'],
            'ciHi': trial['ci_hi']
        },
        'treatment': {'n': trial['n_treatment']},
        'control': {'n': trial['n_control']},
        'baseline': {
            'ageMean': trial['age_mean'],
            'malePercent': trial['male_pct']
        },
        'registration': trial['registration']
    }

    js_ground_truth = f"""{{
            primaryEffect: {{ type: '{trial['effect_type']}', value: {trial['effect_value']}, ciLo: {trial['ci_lo']}, ciHi: {trial['ci_hi']} }},
            treatment: {{ n: {trial['n_treatment']} }},
            control: {{ n: {trial['n_control']} }},
            baseline: {{ ageMean: {trial['age_mean']}, malePercent: {trial['male_pct']} }},
            registration: '{trial['registration']}'
        }}"""

    return f"""    {{
        id: '{trial['id']}',
        source: '{trial['source']}',
        domain: 'Infectious Disease',
        design: '{design}',
        text: `{text}`,
        groundTruth: {js_ground_truth}
    }}"""

